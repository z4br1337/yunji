export function getGrowthPresetLines(dimensionKey, subKey) {
  const dim = GROWTH_SUBOPTION_PRESETS[dimensionKey]
  if (!dim || !subKey) return []
  return dim[subKey] || []
}

const TITLE_MAX = 128

/** 标题写入后端（最长 128）；超长时标题截断，全文写入描述前缀 */
export function buildGrowthSubmitText(presetFull, userDescription) {
  const full = (presetFull || '').trim()
  const desc = (userDescription || '').trim()
  if (full.length <= TITLE_MAX) {
    return { title: full, description: desc }
  }
  return {
    title: `${full.slice(0, TITLE_MAX - 1)}…`,
    description: `【选项全文】${full}\n\n${desc}`.trim(),
  }
}
